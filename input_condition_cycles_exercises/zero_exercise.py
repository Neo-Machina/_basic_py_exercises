risparmio_attuale = int(input('Inserisci la somma che rappresenta il risparmio attuale: \n'))

print(f'Il risparmio attuale è di {risparmio_attuale}')

somma_da_aggiungere = int(input('Inserisci la somma da aggiungere: \n'))

somma_attuale = risparmio_attuale + somma_da_aggiungere

print(f'La nuova somma attuale è {somma_attuale}')
    
